    static let catCollectionIcon = UIImage(named: "catCollectionIcon")
    static let saveIcon = UIImage(named: "saveIcon")
    static let removeIcon = UIImage(named: "removeIcon")
    static let emptyCollectionImage = UIImage(named: "emptyCollectionImage")
}
